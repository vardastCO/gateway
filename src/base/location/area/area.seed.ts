  constructor(
    @InjectRepository(Area)
    private readonly areaRepository: Repository<Area>,
    @InjectDataSource()
    private readonly dataSource: DataSource,
  ) {}

  async run(): Promise<any> {
    const tehranId = (
      await this.dataSource.manager
        .getRepository(City)
        .findOneBy({ slug: "tehran" })
    ).id;

    const tehranAreas = this.data.map(area => {
      return {
        ...area,
        cityId: tehranId,
        isActive: true,
      };
    });

    await this.areaRepository.upsert(tehranAreas, {
      conflictPaths: ["name", "cityId"],
      skipUpdateIfNoValuesChanged: true,
    });
  }
